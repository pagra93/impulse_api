// INICIAR SERVIDOR
// β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•

async function startServer(): Promise<void> {
  try {
    // 1. Probar conexiΓ³n a la base de datos
    console.log('π” Connecting to database...');
    const connected = await testConnection();
    
    if (!connected) {
      console.error('β Could not connect to database');
      process.exit(1);
    }
    
    // 2. Iniciar servidor HTTP
    app.listen(env.PORT, () => {
      console.log('');
      console.log('β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•');
      console.log('  π€ IMPULSE API STARTED');
      console.log('β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•');
      console.log(`  π“ URL:         http://localhost:${env.PORT}`);
      console.log(`  π Environment: ${env.NODE_ENV}`);
      console.log(`  π“¦ Database:    Connected`);
      console.log('β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•');
      console.log('');
    });
    
  } catch (error) {
    console.error('β Failed to start server:', error);
    process.exit(1);
  }
}

// β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•
